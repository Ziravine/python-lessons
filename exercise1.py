samuel_age = 25
peter_age = 32

# equal
print(samuel_age == peter_age)

# addition
print(samuel_age + peter_age)

# subtraction
print(samuel_age-peter_age)

# multiplication
print(samuel_age*peter_age)

# modulus
print(samuel_age % peter_age)

# exponential
print(samuel_age**peter_age)

# greater than or equal to
print(samuel_age >= peter_age)

# less than or equal to
print(samuel_age <= peter_age)

# not equal to
print(samuel_age != peter_age)

# and operator
print(samuel_age >= peter_age and peter_age <= samuel_age)

# or operator
print(samuel_age >= peter_age or peter_age >= samuel_age)
